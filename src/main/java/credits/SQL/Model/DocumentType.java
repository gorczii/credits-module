package credits.SQL.Model;

public class DocumentType {
    private int id;
    private String name;

    public DocumentType(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return this.name;
    }
}
